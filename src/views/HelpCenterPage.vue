            <el-collapse-item title="名词解释" name="14">
                <div class="content">
                    <p><strong>运动距离：</strong>运动距离是指从运动开始（时间）到运动结束（时间），身体移动过的地理距离。</p>
                    <p><strong>运动轨迹：</strong>运动训练学术语，是身体运动构成的重要因素。运动轨迹是指身体的某一部分从开始位置到结束为止所经过的路线组成的动作的空间特征。运动轨迹由运动轨迹方向、运动轨迹形式和运动幅度表示。</p>
                    <p><strong>运动时间：</strong>运动时间则是运动训练学术语，是身体运动构成的重要因素。身体运动时间是指人体完成运动动作所必需的时间，运动的时间与运动速度和运动节奏是密切联系在一起的。当距离一定的情况下，运动速度和运动节奏越快，则运动时间越短；运动速度和运动节奏越慢，则运动时间相应越长。</p>
                    <p><strong>运动速度：</strong>运动训练学术语，指身体或身体某一部分在单位时间内产生的位移距离。</p>
                    <p><strong>运动指标：</strong>
                        运动指标（Amount of exercise）也称“运动负荷”，指人体在体育活动中所承受的生理、心理负荷量以及消耗的热量，由完成练习的运动强度与持续时间，以及动作的准确性和运动方式特点等因素来决定运动指标的大小。
                        <p class="content-indent">从严格意义上讲，体育运动对人体产生的影响并不单纯取决于运动指标，而是运动负荷（“运动指标”只是一种通俗的叫法）。组成运动负荷的主要因素是；“量”和“强度”。</p>
                    </p>
                    <p><strong>卡路里：</strong>卡路里（简称卡，缩写为cal），由英文Calorie音译而来，其定义为在1个大气压下，将1克水提升1摄氏度所需要的热量。人类日常摄取的热量计算，以“卡路里”作为量度单位太小，所以现在的营养学和运动训练普遍采用千卡（俗称大卡，英文 kcal 或 Cal）为单位；1千卡（1大卡）等于1000卡路里，等于4186焦耳。本应用内使用的热量单位，也是千卡。</p>
                    <p><strong>热量消耗：</strong>人类身体能量消耗包括基础代谢、体力活动和食物的热效应三个方面，一般以大卡作为计量单位。
                        <ul>

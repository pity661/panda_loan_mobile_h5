                            <li>消化食物所需要的热量 = 10% × （人体基础代谢的需要的最低热量 +体力活动所需要的热量）</li>
                            <li>成人每日需要的热量 = 1.1 × (人体基础代谢的需要的最低基本热量 +体力活动所需要的热量)</li>
